const mongoose = require('mongoose');

const facilitySchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    order: {
      type: Number,
      default: 0,
    },
  },
  {
    timestamps: true,
  }
);

const Facility = mongoose.model('Facility', facilitySchema);
module.exports = Facility;
